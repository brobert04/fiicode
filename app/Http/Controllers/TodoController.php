    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'todo' => 'required',
            ]);

        $todo = new Todo();
        $todo->doctor_id = auth()->user()->doctor->id;
        $todo->todo = $request->todo;
        $todo->save();
        return \Illuminate\Support\Facades\Response::json($todo);
    }

